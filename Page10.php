  <p><font size="4" face="Arial, Helvetica, sans-serif"><u><b>CONTROL AND INTERACTION</b></u></font></p>
  <p><font size="2" face="Arial, Helvetica, sans-serif">On the screen your window 
    into the world is represented as a room. This room will have<br>
    entrances, exists, lift shafts, walls etc . . . . all of which you will find 
    have 'normal' properties. This means that if you walk across a lift shaft 
    you will fall down it, walk into a wall and you will bounce<br>
    off, walk into a door (these are automatic for conviencence and just to make 
    the coding more <br>
    difficult, we like it that way) and it will open and you can pass through. 
    Just to make things easy<br>
    (and even easier to program, we like it more this way) we make the passing 
    through a door a<br>
    complete event, you cannot stop half way through. This technique of 'implied 
    action' i.e. once<br>
    you have decided to do something such as pick an object up, go through a door, 
    stab somebody<br>
    etc. then from your input up to the conclusion of that action is controlled 
    (most of the time (we<br>
    like it anyway)) by the program.</font></p>
  <p><font size="2" face="Arial, Helvetica, sans-serif">When you meet a character 
    in a room he will either ignore you or you may enter a 'situation'. In<br>
    a situation, and heavily dependent on the circumstances prevalent at the time 
    one of the<br>
    following may occur:</font></p>
  <p><font size="2" face="Arial, Helvetica, sans-serif">Whilst you may not be 
    involved in a normal converstaion, you may observe others talking. Watch<br>
